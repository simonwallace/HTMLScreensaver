namespace HTMLScreensaver
{
    /// <summary>
    /// The available modes to display the screensaver.
    /// </summary>
    public enum Mode
    {
        /// <summary>Configure the screensaver settings.</summary>
        CONFIGURE = 'c',

        /// <summary>Preview the screensaver.</summary>
        PREVIEW = 'p',

        /// <summary>Show the screensaver.</summary>
        SHOW = 's',

        /// <summary>The screensaver mode is unspecified.</summary>
        UNDEFINED = '0'
    }
}